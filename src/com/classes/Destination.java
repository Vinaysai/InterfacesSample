package com.classes;

public class Destination {

	String a;
	String b;

	public String getA() {
		return a;
	}

	public void setA(String a) {
		this.a = a;
	}

	@Override
	public String toString() {
		return "Destination [a=" + a + ", b=" + b + "]";
	}

	public String getB() {
		return b;
	}

	public void setB(String b) {
		this.b = b;
	}

}
